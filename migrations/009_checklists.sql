-- Интерактивные чек-листы (пункты с группами) + опциональный PDF.
-- Схема pravo, управление через админку (по образцу research_reports).
CREATE TABLE IF NOT EXISTS checklists (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  slug VARCHAR(120) UNIQUE NOT NULL,
  title VARCHAR(500) NOT NULL,
  description TEXT,                       -- короткий тизер для карточки
  category VARCHAR(120),
  icon VARCHAR(60),                       -- имя иконки lucide-react
  accent VARCHAR(20) DEFAULT 'cyan',      -- 'cyan' | 'magenta'
  intro TEXT,                             -- вводный текст на странице чек-листа
  items JSONB,                            -- [{ "group": "...", "points": [{"text":"...","hint":"..."}] }]
  pdf_url TEXT,                           -- внешняя ссылка ИЛИ /reports|/checklists/...pdf
  display_order INTEGER DEFAULT 0,
  is_published BOOLEAN DEFAULT TRUE,
  created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

CREATE INDEX IF NOT EXISTS idx_checklists_slug ON checklists(slug);
CREATE INDEX IF NOT EXISTS idx_checklists_published ON checklists(is_published, display_order);

